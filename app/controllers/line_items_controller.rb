class LineItemsController < ApplicationController
  load_and_authorize_resource
  before_action :set_cart, only: [:new, :create, :update]
  before_action :set_line_item, only: [:show, :edit, :update, :destroy]

  # GET /line_items
  # GET /line_items.json
